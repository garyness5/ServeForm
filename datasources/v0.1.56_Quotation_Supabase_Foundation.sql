-- -----------------------------------------------------
-- sync_quotation_eligibility
--
-- Purpose:
--   Maintains Event-centered Sales inbox.
--   Adds eligible Events to QuotationList.
--
-- Called from:
--   QuotationList / sync action / later Event status workflows.
--
-- Notes:
--   Does not delete/deactivate quote documents.
-- -----------------------------------------------------
create or replace function public.sync_quotation_eligibility(
  p_client_id uuid
)
returns table(
  inserted_count integer,
  updated_count integer
)
language plpgsql
as $function$
declare
  v_inserted integer := 0;
  v_updated integer := 0;
begin
  insert into public.quo_event_inbox
  (
    client_id,
    event_id,
    hidden
  )
  select
    e.client_id,
    e.id,
    false
  from public.evt_items e
  where e.client_id = p_client_id
    and e.status in ('Sent', 'Ordered', 'Accepted')
    and coalesce(e.deleted, false) = false
  on conflict (client_id, event_id) do nothing;

  get diagnostics v_inserted = row_count;

  update public.quo_event_inbox qi
  set
    hidden = false,
    updated_at = now()
  where qi.client_id = p_client_id
    and qi.hidden = true
    and exists (
      select 1
      from public.evt_items e
      where e.id = qi.event_id
        and e.client_id = qi.client_id
        and e.status in ('Sent', 'Ordered', 'Accepted')
        and coalesce(e.deleted, false) = false
    );

  get diagnostics v_updated = row_count;

  return query select v_inserted, v_updated;
end;
$function$;


-- -----------------------------------------------------
-- refresh_quotation_menus
--
-- Purpose:
--   Refreshes Draft quote menu rows from current Event.
--
-- Called from:
--   import_event_to_quote()
--
-- Notes:
--   Preserves manual price_per_guest and notes when the same
--   event_component_id still exists.
-- -----------------------------------------------------
create or replace function public.refresh_quotation_menus(
  p_quote_event_id bigint
)
returns table(menu_rows integer)
language plpgsql
as $function$
declare
  v_client_id uuid;
  v_event_id bigint;
  v_rows integer := 0;
begin
  select
    client_id,
    event_id
  into
    v_client_id,
    v_event_id
  from public.quo_events
  where id = p_quote_event_id
    and quote_status = 'Draft'
    and coalesce(deleted, false) = false;

  if v_client_id is null then
    raise exception 'Draft quotation not found or quote is locked.';
  end if;

  create temporary table tmp_quo_menus_existing on commit drop as
  select *
  from public.quo_menus
  where quote_event_id = p_quote_event_id;

  delete from public.quo_menus
  where quote_event_id = p_quote_event_id;

  insert into public.quo_menus
  (
    quote_event_id,
    client_id,
    event_id,
    event_component_id,
    menu_id,
    line_no,
    menu_name,
    guests,
    cost,
    price_per_guest,
    notes,
    allergen_names,
    diet_tag_names,
    active
  )
  select
    p_quote_event_id,
    v_client_id,
    v_event_id,
    ec.id,
    ec.menu_id,
    ec.line_no,
    ec.menu_name,
    ec.guests,
    ec.line_cost,
    old.price_per_guest,
    old.notes,
    ec.allergen_names,
    ec.diet_tag_names,
    true
  from public.vw_evt_components ec
  left join tmp_quo_menus_existing old
    on old.event_component_id = ec.id
  where ec.event_id = v_event_id
    and ec.active = true
    and ec.menu_id is not null
  order by ec.line_no;

  get diagnostics v_rows = row_count;

  return query select v_rows;
end;
$function$;


-- -----------------------------------------------------
-- ensure_quotation_deposits
--
-- Purpose:
