    event_price_total,
    service_charge,
    final_due_date,
    imported_event_revision_no,
    last_update_all_at,
    customer_notes,
    internal_notes,
    terms,
    true,
    false
  from public.quo_events
  where id = p_quote_event_id
    and coalesce(deleted, false) = false
  returning id into v_new_id;

  if v_new_id is null then
    raise exception 'Quote not found.';
  end if;

  insert into public.quo_menus
  (
    quote_event_id,
    client_id,
    event_id,
    event_component_id,
    menu_id,
    line_no,
    menu_name,
    guests,
    cost,
    price_per_guest,
    notes,
    allergen_names,
    diet_tag_names,
    active
  )
  select
    v_new_id,
    client_id,
    event_id,
    event_component_id,
    menu_id,
    line_no,
    menu_name,
    guests,
    cost,
    price_per_guest,
    notes,
    allergen_names,
    diet_tag_names,
    active
  from public.quo_menus
  where quote_event_id = p_quote_event_id
  order by line_no;

  insert into public.quo_deposits
  (
    quote_event_id,
    client_id,
    line_no,
    label,
    amount,
    due_date,
    notes
  )
  select
    v_new_id,
    client_id,
    line_no,
    label,
    amount,
    due_date,
    notes
  from public.quo_deposits
  where quote_event_id = p_quote_event_id
  order by line_no;

  return query select v_new_id;
end;
$function$;


-- -----------------------------------------------------
-- issue_quote
--
-- Purpose:
--   Locks a Draft quote and assigns official quote number.
--
-- Notes:
--   Supabase owns numbering.
--   Appsmith only calls Issue.
-- -----------------------------------------------------
create or replace function public.issue_quote(
  p_quote_event_id bigint
)
returns table(
  quote_event_id bigint,
  quote_number text,
  quote_status text
)
language plpgsql
as $function$
declare
  v_client_id uuid;
  v_quote_number text;
begin
  select q.client_id
  into v_client_id
  from public.quo_events q
  where q.id = p_quote_event_id
    and q.quote_status = 'Draft'
    and coalesce(q.deleted, false) = false;

  if v_client_id is null then
    raise exception 'Draft quote not found or quote is already locked.';
  end if;

  if not exists (
    select 1
    from public.quo_menus qm
    where qm.quote_event_id = p_quote_event_id
      and qm.active = true
  ) then
    raise exception 'Cannot issue a quote without menu rows.';
  end if;

  v_quote_number := public.get_next_document_number(v_client_id, 'quote');

  update public.quo_events q
  set
    quote_status = 'Issued',
    quote_number = v_quote_number,
    issued_at = coalesce(q.issued_at, now()),
    updated_at = now()
  where q.id = p_quote_event_id;

  return query
  select
    q.id,
    q.quote_number,
    q.quote_status
  from public.quo_events q
  where q.id = p_quote_event_id;
end;
$function$;


-- -----------------------------------------------------
