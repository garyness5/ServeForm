drop function if exists public.get_next_document_number(uuid, text);

create or replace function public.get_next_document_number(
  p_client_id uuid,
  p_document_type text
)
returns text
language plpgsql
as $function$
declare
  v_prefix text;
  v_next_number integer;
  v_padding integer;
  v_document_number text;
begin
  select
    ds.prefix,
    ds.next_number,
    ds.padding
  into
    v_prefix,
    v_next_number,
    v_padding
  from public.document_sequences ds
  where ds.client_id = p_client_id
    and ds.document_type = p_document_type
    and ds.active = true
  for update;

  if v_next_number is null then
    raise exception 'Document numbering settings not found for %.%', p_client_id, p_document_type;
  end if;

  v_document_number :=
    coalesce(v_prefix, '') ||
    lpad(v_next_number::text, greatest(coalesce(v_padding, 0), 0), '0');

  update public.document_sequences ds
  set
    next_number = v_next_number + 1,
    updated_at = now()
  where ds.client_id = p_client_id
    and ds.document_type = p_document_type
    and ds.active = true;

  return v_document_number;
end;
$function$;


