create or replace function public.update_quote_header(
  p_quote_event_id bigint,
  p_quote_reference text default null,
  p_valid_until date default null,
  p_pricing_mode text default 'per_menu',
  p_event_price_total numeric default null,
  p_service_charge numeric default null,
  p_final_due_date date default null,
  p_customer_notes text default null,
  p_internal_notes text default null,
  p_terms text default null
)
returns table(
  quote_event_id bigint,
  quote_status text,
  pricing_mode text,
  quote_total numeric,
  balance numeric
)
language plpgsql
as $function$
begin
  if p_pricing_mode not in ('per_menu', 'event_total') then
    raise exception 'Invalid pricing mode: %', p_pricing_mode;
  end if;

  update public.quo_events q
  set
    quote_reference = nullif(trim(p_quote_reference), ''),
    valid_until = p_valid_until,
    pricing_mode = p_pricing_mode,
    event_price_total =
      case
        when p_pricing_mode = 'event_total' then p_event_price_total
        else null
      end,
    service_charge = p_service_charge,
    final_due_date = p_final_due_date,
    customer_notes = nullif(trim(p_customer_notes), ''),
    internal_notes = nullif(trim(p_internal_notes), ''),
    terms = nullif(trim(p_terms), ''),
    updated_at = now()
  where q.id = p_quote_event_id
    and q.quote_status = 'Draft'
    and coalesce(q.deleted, false) = false;

  if not found then
    raise exception 'Draft quote not found or quote is locked.';
  end if;

  return query
  select
    v.quote_event_id,
    v.quote_status,
    v.pricing_mode,
    v.quote_total,
    v.balance
  from public.vw_quo_events v
  where v.quote_event_id = p_quote_event_id;
end;
$function$;

