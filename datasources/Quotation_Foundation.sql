    due_date,
    notes
  from public.quo_deposits
  where quote_event_id = p_quote_event_id
  order by line_no;

  return query select v_new_id;
end;
$function$;

-- -------------------------------------------------------------
-- Issue Draft quote.
-- Supabase owns quote number generation.
-- -------------------------------------------------------------
create or replace function public.issue_quote(
  p_quote_event_id bigint
)
returns table(
  quote_event_id bigint,
  quote_number text,
  quote_status text
)
language plpgsql
as $function$
declare
  v_client_id uuid;
  v_quote_number text;
begin
  select q.client_id
  into v_client_id
  from public.quo_events q
  where q.id = p_quote_event_id
    and q.quote_status = 'Draft'
    and coalesce(q.deleted, false) = false;

  if v_client_id is null then
    raise exception 'Draft quote not found or quote is already locked.';
  end if;

  if not exists (
    select 1
    from public.quo_menus qm
    where qm.quote_event_id = p_quote_event_id
      and qm.active = true
  ) then
    raise exception 'Cannot issue a quote without menu rows.';
  end if;

  v_quote_number := public.get_next_document_number(v_client_id, 'quote');

  update public.quo_events q
  set
    quote_status = 'Issued',
    quote_number = v_quote_number,
    issued_at = coalesce(q.issued_at, now()),
    updated_at = now()
  where q.id = p_quote_event_id;

  return query
  select
    q.id,
    q.quote_number,
    q.quote_status
  from public.quo_events q
  where q.id = p_quote_event_id;
end;
$function$;

-- -------------------------------------------------------------
-- Accept Issued quote.
-- Existing Accepted quote for same Event is returned to Issued.
-- Event status becomes Accepted.
-- -------------------------------------------------------------
create or replace function public.accept_quote(
  p_quote_event_id bigint
)
returns table(
  quote_event_id bigint,
  event_id bigint,
  quote_number text,
  quote_status text
)
language plpgsql
as $function$
declare
  v_client_id uuid;
  v_event_id bigint;
begin
  select
    q.client_id,
    q.event_id
  into
    v_client_id,
    v_event_id
  from public.quo_events q
  where q.id = p_quote_event_id
    and q.quote_status = 'Issued'
    and coalesce(q.deleted, false) = false;

  if v_client_id is null then
    raise exception 'Only an issued quote can be accepted.';
  end if;

  update public.quo_events q
  set
    quote_status = 'Issued',
    updated_at = now()
  where q.client_id = v_client_id
    and q.event_id = v_event_id
    and q.id <> p_quote_event_id
    and q.quote_status = 'Accepted'
    and coalesce(q.deleted, false) = false;

  update public.quo_events q
  set
    quote_status = 'Accepted',
    accepted_at = now(),
    updated_at = now()
  where q.id = p_quote_event_id;

  update public.evt_items e
  set
    status = 'Accepted',
    status_changed_at = now(),
    updated_at = now()
  where e.id = v_event_id
    and e.client_id = v_client_id
    and coalesce(e.deleted, false) = false;

  return query
  select
    q.id,
    q.event_id,
    q.quote_number,
    q.quote_status
  from public.quo_events q
  where q.id = p_quote_event_id;
end;
$function$;

