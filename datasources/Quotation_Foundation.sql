-- -------------------------------------------------------------
-- Import Event into Quote.
-- If p_quote_event_id is null: create new Draft.
-- If supplied: update that existing Draft.
-- -------------------------------------------------------------
create or replace function public.import_event_to_quote(
  p_client_id uuid,
  p_event_id bigint,
  p_quote_event_id bigint default null
)
returns table(
  quote_event_id bigint,
  imported_event_revision_no integer,
  menu_rows integer,
  deposit_rows integer
)
language plpgsql
as $function$
declare
  v_quote_event_id bigint;
  v_event_revision_no integer;
  v_menu_rows integer := 0;
  v_deposit_rows integer := 0;
begin
  select quotation_revision_no
  into v_event_revision_no
  from public.evt_items
  where id = p_event_id
    and client_id = p_client_id
    and coalesce(deleted, false) = false;

  if v_event_revision_no is null then
    raise exception 'Event not found.';
  end if;

  if p_quote_event_id is not null then
    select id
    into v_quote_event_id
    from public.quo_events
    where id = p_quote_event_id
      and client_id = p_client_id
      and event_id = p_event_id
      and quote_status = 'Draft'
      and coalesce(deleted, false) = false;

    if v_quote_event_id is null then
      raise exception 'Draft quote not found or quote is locked.';
    end if;
  else
    insert into public.quo_events
    (
      client_id,
      event_id,
      quote_status,
      pricing_mode,
      imported_event_revision_no,
      quote_date,
      active,
      deleted
    )
    values
    (
      p_client_id,
      p_event_id,
      'Draft',
      'per_menu',
      v_event_revision_no,
      current_date,
      true,
      false
    )
    returning id into v_quote_event_id;
  end if;

  update public.quo_events
  set
    imported_event_revision_no = v_event_revision_no,
    last_update_all_at = now(),
    updated_at = now()
  where id = v_quote_event_id;

  select r.menu_rows
  into v_menu_rows
  from public.refresh_quotation_menus(v_quote_event_id) r;

  select d.deposit_rows
  into v_deposit_rows
  from public.ensure_quotation_deposits(v_quote_event_id) d;

  update public.quo_event_inbox
  set
    last_seen_event_revision_no = v_event_revision_no,
    updated_at = now()
  where client_id = p_client_id
    and event_id = p_event_id;

  return query select
    v_quote_event_id,
    v_event_revision_no,
    coalesce(v_menu_rows, 0),
    coalesce(v_deposit_rows, 0);
end;
$function$;

-- -------------------------------------------------------------
-- Single entry point for UI Kitchen Update actions.
-- p_action: replace_current | create_new
-- -------------------------------------------------------------
create or replace function public.update_quote_from_kitchen(
  p_quote_event_id bigint,
  p_action text
)
returns table(
  quote_event_id bigint,
  event_id bigint,
  action_taken text,
  imported_event_revision_no integer,
  menu_rows integer,
  deposit_rows integer
)
language plpgsql
as $function$
declare
  v_client_id uuid;
  v_event_id bigint;
  v_new_quote_event_id bigint;
  v_event_revision_no integer;
  v_menu_rows integer := 0;
  v_deposit_rows integer := 0;
begin
  if p_action not in ('replace_current', 'create_new') then
    raise exception 'Invalid kitchen update action: %', p_action;
  end if;

  select
    q.client_id,
    q.event_id
  into
    v_client_id,
    v_event_id
  from public.quo_events q
  where q.id = p_quote_event_id
    and q.quote_status = 'Draft'
    and coalesce(q.deleted, false) = false;

  if v_client_id is null then
    raise exception 'Draft quote not found or quote is locked.';
  end if;

  select e.quotation_revision_no
  into v_event_revision_no
  from public.evt_items e
  where e.id = v_event_id
    and e.client_id = v_client_id
    and coalesce(e.deleted, false) = false;

  if v_event_revision_no is null then
    raise exception 'Event not found.';
  end if;

  if p_action = 'replace_current' then
    select
      i.quote_event_id,
      i.imported_event_revision_no,
      i.menu_rows,
      i.deposit_rows
    into
      v_new_quote_event_id,
      v_event_revision_no,
      v_menu_rows,
      v_deposit_rows
    from public.import_event_to_quote(
      v_client_id,
      v_event_id,
      p_quote_event_id
    ) i;

    return query select
      v_new_quote_event_id,
      v_event_id,
      'replace_current'::text,
      v_event_revision_no,
      v_menu_rows,
      v_deposit_rows;
    return;
  end if;

  if p_action = 'create_new' then
    select
      i.quote_event_id,
      i.imported_event_revision_no,
      i.menu_rows,
      i.deposit_rows
    into
      v_new_quote_event_id,
      v_event_revision_no,
      v_menu_rows,
      v_deposit_rows
    from public.import_event_to_quote(
      v_client_id,
      v_event_id,
      null
    ) i;

    return query select
      v_new_quote_event_id,
      v_event_id,
      'create_new'::text,
      v_event_revision_no,
      v_menu_rows,
      v_deposit_rows;
    return;
  end if;
end;
$function$;

