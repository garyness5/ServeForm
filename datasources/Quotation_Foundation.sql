create or replace view public.vw_quo_event_inbox as
select
  qi.id as inbox_id,
  qi.client_id,
  qi.event_id,
  e.name as event_name,
  e.event_ref,
  e.event_date,
  e.event_datetime,
  e.status as event_status,
  e.format,
  e.total_guests,
  e.total_cost,
  e.customer_id,
  e.customer_name,
  e.company,
  e.contact_id,
  e.contact_person,
  e.venue_id,
  e.venue_name,
  ei.quotation_revision_no,
  qi.last_seen_event_revision_no,
  qi.hidden,
  case
    when exists (
      select 1
      from public.quo_events q
      where q.client_id = qi.client_id
        and q.event_id = qi.event_id
        and coalesce(q.deleted, false) = false
        and q.quote_status = 'Draft'
        and coalesce(q.imported_event_revision_no, 0) < coalesce(ei.quotation_revision_no, 1)
    )
    then true
    when not exists (
      select 1
      from public.quo_events q
      where q.client_id = qi.client_id
        and q.event_id = qi.event_id
        and coalesce(q.deleted, false) = false
    )
    then true
    else false
  end as kitchen_update_available,
  (
    select count(*)
    from public.quo_events q
    where q.client_id = qi.client_id
      and q.event_id = qi.event_id
      and coalesce(q.deleted, false) = false
      and q.quote_status = 'Draft'
  ) as draft_quote_count,
  (
    select count(*)
    from public.quo_events q
    where q.client_id = qi.client_id
      and q.event_id = qi.event_id
      and coalesce(q.deleted, false) = false
      and q.quote_status = 'Issued'
  ) as issued_quote_count,
  (
    select count(*)
    from public.quo_events q
    where q.client_id = qi.client_id
      and q.event_id = qi.event_id
      and coalesce(q.deleted, false) = false
      and q.quote_status = 'Accepted'
  ) as accepted_quote_count,
  (
    select q.quote_number
    from public.quo_events q
    where q.client_id = qi.client_id
      and q.event_id = qi.event_id
      and coalesce(q.deleted, false) = false
      and q.quote_status = 'Accepted'
    order by q.accepted_at desc nulls last, q.id desc
    limit 1
  ) as accepted_quote_number,
  qi.created_at,
  qi.updated_at
from public.quo_event_inbox qi
join public.evt_items ei
  on ei.id = qi.event_id
 and ei.client_id = qi.client_id
join public.vw_evt_items e
  on e.id = qi.event_id
 and e.client_id = qi.client_id
where qi.hidden = false
  and coalesce(ei.deleted, false) = false;

