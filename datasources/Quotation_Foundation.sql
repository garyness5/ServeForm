-- =============================================================
-- SAVVEYRA - QUOTATION FOUNDATION
-- Version: v0.1.56
-- Purpose: Supabase foundation for Event-centered Sales / Quotation workflow
-- Date: 2026-07-07
-- =============================================================
--
-- Core model:
-- - QuotationList is Event-centered: one inbox row per Event.
-- - One Event may have many Quotes.
-- - Quotes move Draft -> Issued -> Accepted.
-- - Only Draft quotes are editable.
-- - Issue assigns quote_number through Supabase-owned numbering.
-- - Accepting one quote returns any previous Accepted quote for same Event to Issued.
-- - Kitchen Updates are tracked using evt_items.quotation_revision_no and quo_events.imported_event_revision_no.
-- - Appsmith owns UI; Supabase owns business rules.
-- =============================================================

