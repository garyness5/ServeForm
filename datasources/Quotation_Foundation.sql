create or replace view public.vw_quo_events as
select
  qe.id as quote_event_id,
  qe.client_id,
  qe.event_id,
  e.name as event_name,
  e.event_ref,
  e.event_date,
  e.event_time,
  e.event_datetime,
  e.status as event_status,
  e.format,
  e.total_guests_manual,
  e.total_guests,
  e.total_cost as event_cost,
  e.customer_id,
  e.customer_name,
  e.company,
  e.contact_id,
  e.contact_person,
  e.phone,
  e.email,
  e.venue_id,
  e.venue_name,
  qe.quote_status,
  qe.quote_number,
  qe.quote_reference,
  qe.quote_date,
  qe.valid_until,
  qe.pricing_mode,
  qe.event_price_total,
  qe.service_charge,
  qe.final_due_date,
  qe.issued_at,
  qe.accepted_at,
  qe.imported_event_revision_no,
  qe.last_update_all_at,
  qe.customer_notes,
  qe.internal_notes,
  qe.terms,
  case
    when qe.pricing_mode = 'event_total'
      then coalesce(qe.event_price_total, 0)
    else coalesce((
      select sum(coalesce(qm.guests, 0) * coalesce(qm.price_per_guest, 0))
      from public.quo_menus qm
      where qm.quote_event_id = qe.id
        and qm.active = true
    ), 0)
  end as subtotal,
  coalesce(qe.service_charge, 0) as service_charge_amount,
  case
    when qe.pricing_mode = 'event_total'
      then coalesce(qe.event_price_total, 0)
    else coalesce((
      select sum(coalesce(qm.guests, 0) * coalesce(qm.price_per_guest, 0))
      from public.quo_menus qm
      where qm.quote_event_id = qe.id
        and qm.active = true
    ), 0)
  end + coalesce(qe.service_charge, 0) as quote_total,
  (
    case
      when qe.pricing_mode = 'event_total'
        then coalesce(qe.event_price_total, 0)
      else coalesce((
        select sum(coalesce(qm.guests, 0) * coalesce(qm.price_per_guest, 0))
        from public.quo_menus qm
        where qm.quote_event_id = qe.id
          and qm.active = true
      ), 0)
    end + coalesce(qe.service_charge, 0)
  )
  - coalesce((
      select sum(coalesce(qd.amount, 0))
      from public.quo_deposits qd
      where qd.quote_event_id = qe.id
        and qd.line_no in (1,2,3)
    ), 0) as balance,
  qe.active,
  qe.deleted,
  qe.created_at,
  qe.updated_at
from public.quo_events qe
join public.vw_evt_items e
  on e.id = qe.event_id
 and e.client_id = qe.client_id
where coalesce(qe.deleted, false) = false;

